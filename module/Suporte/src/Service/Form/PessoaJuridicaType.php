namespace Rtd\Suporte\Service\Form;


use Helpers\FormType\Choices\Estados;
use Helpers\FormType\Choices\Nacionalidade;
use Helpers\FormType\Choices\Pais;
use Helpers\Formulario\FormType;
use Rtd\Suporte\Entity\Central\PessoaJuridica;
use Symfony\Component\Form\Extension\Core\Type\ButtonType;
use Symfony\Component\Form\Extension\Core\Type\ChoiceType;
use Symfony\Component\Form\Extension\Core\Type\DateType;
use Symfony\Component\Form\Extension\Core\Type\SubmitType;
use Symfony\Component\Form\Extension\Core\Type\TextType;
use Symfony\Component\Form\FormBuilderInterface;
use Symfony\Component\OptionsResolver\OptionsResolver;


class PessoaJuridicaType extends FormType
{

    public function buildForm(FormBuilderInterface $builder, array $options)
    {
        return $builder
            ->add('ni',ChoiceType::class,[
                'label'=>'Pessoa Juridica',
                    'attr'=>[
                        'class'=>'select2-ajax-pessoa',
                        'required'=>'required'
                    ]
                ]
            )
            ->add('nacionalidadeCapital',ChoiceType::class,(new Nacionalidade())->make())
            ->add('autorizacaoFuncionamento',TextType::class,[
                'required'=>false
            ])
            ->add('pais',ChoiceType::class,(new Pais())->make())
            ->add('participacaoCapital',TextType::class,[
                'attr'=>[
                    'class'=>'money',
                    'maxlength'=>50,
                ]
            ])
            ->add('dataAbertura',DateType::class,[
                'widget'=>'single_text',
                'format'=>'yyyy-MM-dd',
                'attr'=>[
                    'class'=>'data'
                ]
            ])
            ->add('ufSede',ChoiceType::class,(new Estados())->make());
    }

    public function configureOptions(OptionsResolver $resolver)
    {

        $resolver->setDefaults([
           'data_class'=>PessoaJuridica::class
        ]);
        parent::configureOptions($resolver); // TODO: Change the autogenerated stub
    }

}